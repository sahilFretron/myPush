async function updateDocuments(payload) {
    try {
        let url = `https://apis.fretron.com/documents/v2/documents`;
        console.log(url);
        let res = await rp({
            uri: url,
            method: "PUT",
            body: payload,
            json: true,
            headers: {
                authorization: token,
            },
        });

        if (res.error) {
            console.log(`Some error in updating document- ${res.error}`);
        }

        return res.status == 200 ? res.data : null;
    } catch (e) {
        console.log(`Updating Document Error ${e.message}`);
    }

    return null;
}

